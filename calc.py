import math
def add(num1,num2):
    return num1+num2
def sub(num1,num2):
    return num1-num2
def mul(num1,num2):
    return num1 * num2
def div(num1,num2):
    return num1/num2
def pwr(num1,num2):
    return num1**num2
def modulus(num1,num2):
    return num1%num2
def root(num1,num2):
    return num2**(1/num1)
def ln(num1):
    return math.log(num1)
def sin(num1):
    return math.sin(num1)
def cos(num1):
    return math.cos(num1)
def tan(num1):
    return math.tan(num1)

